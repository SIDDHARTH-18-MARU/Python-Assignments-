'''
In this question we are taking two numbers as input i.e. x and y. For these two numbers we are performing operations like 
addition, subtraction, multiplication, division.
'''
x = int(input("Enter first number:"))
y = int(input("Enter second number:"))
add = x + y
subtract = x - y
multiply = x * y
divi = x / y
print("Addition = ",add)
print("Subtraction = ",subtract)
print("Multiplication = ",multiply)
print("Division = ",divi)
